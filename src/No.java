public class No {
    private String info;
    private boolean isFolha;

    public No (String info){
        this.info = info;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public boolean isFolha() {
        return isFolha;
    }

    public void setFolha(boolean folha) {
        isFolha = folha;
    }
}
